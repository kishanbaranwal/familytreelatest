package com.kishan.bean;

public class Area {

	private int arid;
	
	private int tehid;
	
	private String area;
	
	private Tehseel tehs;

	public int getArid() {
		return arid;
	}

	public void setArid(int arid) {
		this.arid = arid;
	}



	public String getArea() {
		return area;
	}

	public void setArea(String area) {
		this.area = area;
	}

	public int getTehid() {
		return tehid;
	}

	public void setTehid(int tehid) {
		this.tehid = tehid;
	}

	public Tehseel getTehs() {
		return tehs;
	}

	public void setTehs(Tehseel tehs) {
		this.tehs = tehs;
	}

	
	
	
}
