package com.kishan.bean;



public class Village {

	private int vid;
	
	private int tehid;
	
	private String village;
	
	private Tehseel tehs;
	

	public int getVid() {
		return vid;
	}

	public void setVid(int vid) {
		this.vid = vid;
	}

	public int getTehid() {
		return tehid;
	}

	public void setTehid(int tehid) {
		this.tehid = tehid;
	}

	public String getVillage() {
		return village;
	}

	public void setVillage(String village) {
		this.village = village;
	}

	public Tehseel getTehs() {
		return tehs;
	}

	public void setTehs(Tehseel tehs) {
		this.tehs = tehs;	
	}

	
	
	
}
